public class Nested_if {
    public static void main(String [] args){
        int a = 12 ;
        int b = 12 ;

        if(a>b){
            System.out.println("a is greater than b.");
        }
        if(a<b){
            System.out.println("b is greater than a.") ;
        }
        else{
            System.out.println("a and b are both equal.");
        }
    }
}

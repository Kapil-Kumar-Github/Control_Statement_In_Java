public class Switch_Control_Statement {
    public static void main(String [] args){
        int num = 6;
        switch(num){
            case 0 :
            System.out.println("value be 0");
            break ;
            case 1:
                System.out.println("value be 1") ;
                break ;
            case 69:
                System.out.println("value be 2") ;
                break ;
            case 3 :
                System.out.println("value be 3");
                break ;
            default :
                System.out.println("num be " + num);

        }
    }
}
